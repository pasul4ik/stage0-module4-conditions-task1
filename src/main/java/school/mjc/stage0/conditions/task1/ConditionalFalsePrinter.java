package school.mjc.stage0.conditions.task1;

public class ConditionalFalsePrinter {
    public static void main(String[] args) {
        boolean condition = false;
        if (condition == true) {
            System.out.println("true");
        }
        else  {
            System.out.println("false");
        }
    }
}

